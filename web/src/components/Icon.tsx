// Maps a free-text category name to one of the line icons above.
export function iconForCategory(category: string | null): IconName {
  const c = (category || "").toLowerCase();
  if (/pub|bar/.test(c)) return "beer";
  if (/cafe|coffee/.test(c)) return "coffee";
  if (/restaurant|fast|food|takeaway/.test(c)) return "utensils";
  if (/hotel|guest|hostel|lodg|accommod/.test(c)) return "bed";
  if (/fuel|petrol/.test(c)) return "fuel";
  if (/bank|financ|insur/.test(c)) return "bank";
  if (/pharmac|chemist/.test(c)) return "pill";
  if (/supermarket|convenience|grocer|shop|store|retail|gift/.test(c)) return "cart";
  if (/hair|beauty|salon|spa/.test(c)) return "scissors";
  if (/car|garage|repair|tyre|motor/.test(c)) return "car";
  if (/doctor|dentist|clinic|vet|health|medic/.test(c)) return "health";
  if (/book|library/.test(c)) return "book";
  if (/school|college|educat|train/.test(c)) return "school";
  if (/build|construct|plumb|electric|trade|doityourself|diy/.test(c)) return "wrench";
  if (/gym|fitness|sport/.test(c)) return "dumbbell";
  return "building";
}
